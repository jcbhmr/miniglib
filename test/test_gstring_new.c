#include <stdio.h>
#include <stdlib.h>
#include <stdbool.h>
#include <string.h>
#include <gstring.h>

#define release_assert(EX) (void)((EX) || (__assert (#EX, __FILE__, __LINE__),0))
extern void __assert (const char *msg, const char *file, int line);

unsigned char test_wasm[] = {
  0x00, 0x61, 0x73, 0x6d, 0x01, 0x00, 0x00, 0x00, 0x01, 0x07, 0x01, 0x60,
  0x02, 0x7f, 0x7f, 0x01, 0x7f, 0x03, 0x02, 0x01, 0x00, 0x07, 0x0a, 0x01,
  0x06, 0x61, 0x64, 0x64, 0x54, 0x77, 0x6f, 0x00, 0x00, 0x0a, 0x09, 0x01,
  0x07, 0x00, 0x20, 0x00, 0x20, 0x01, 0x6a, 0x0b, 0x00, 0x0a, 0x04, 0x6e,
  0x61, 0x6d, 0x65, 0x02, 0x03, 0x01, 0x00, 0x00
};
unsigned int test_wasm_len = 56;

int main() {
    GString* a = g_string_new("\0asm");
    release_assert(a);
    release_assert(a->len == 4);
    g_string_free(a, true);

    GString* b = g_string_new_len(test_wasm, test_wasm_len);
    release_assert(b);
    release_assert(b->len == test_wasm_len);
    g_string_free(b, true);
    return 0;
}
